a=int(input("Enter a number>>"))
if (a==1):
    print("Sunday")
elif(a==2):
    print("Monday")
elif(a==3):
    print("Tuesday")
elif(a==4):
    print("Wednesday")
elif(a==5):
    print("Thursday")
elif(a==6):
    print("Friday")
elif(a==7):
    print("Saturday")
else:
    print("Invalid Input!!")
